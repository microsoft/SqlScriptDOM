CREATE MATERIALIZED VIEW View1MCD
WITH (DISTRIBUTION = HASH(Col4, Col5), FOR_APPEND)
AS
SELECT MAX(Col1) AS Max_Col1,
       MIN(Col2) AS Min_Col2,
       Col3,
       Col4,
       Col5
FROM dbo.Table1
GROUP BY Col3, Col4, Col5;


GO
