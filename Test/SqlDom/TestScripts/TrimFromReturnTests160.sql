CREATE FUNCTION dbo.TrimUnicodeSpace(@str NVARCHAR(MAX))
RETURNS NVARCHAR(MAX)
AS
BEGIN
    RETURN TRIM(NCHAR(12288) FROM @str);
END;
GO

CREATE FUNCTION dbo.TrimLeadingX(@str NVARCHAR(MAX))
RETURNS NVARCHAR(MAX)
AS
BEGIN
    RETURN TRIM(LEADING 'x' FROM @str);
END;
GO

CREATE FUNCTION dbo.TrimTrailingX(@str NVARCHAR(MAX))
RETURNS NVARCHAR(MAX)
AS
BEGIN
    RETURN TRIM(TRAILING 'x' FROM @str);
END;
GO

CREATE FUNCTION dbo.TrimBothX(@str NVARCHAR(MAX))
RETURNS NVARCHAR(MAX)
AS
BEGIN
    RETURN TRIM(BOTH 'x' FROM @str);
END;
GO
